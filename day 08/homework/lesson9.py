# **1) for ციკლი 1-დან 10-მდე რიცხვების გამოსატანად**
for num in range(1, 11):  # 1-დან 10-მდე
    print(num)  # თითოეული რიცხვი დაიბეჭდება

# **2) "Hello world" 5-ჯერ ახალ ხაზზე**
for _ in range(5):  # 5-ჯერ გამეორება
    print("Hello world")  # ყოველი გამეორებით ახალი ხაზი

# **3) მხოლოდ ლუწი რიცხვების დაბეჭდვა**
for num in range(2, 11, 2):  # ვიწყებთ 2-დან, ვმატებთ 2-ს ყოველ ჯერზე
    print(num)


