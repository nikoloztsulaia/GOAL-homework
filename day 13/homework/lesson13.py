#  წნევის ანალიზი
# მომხმარებელი შეიყვანს ორ მაჩვენებელს: სისტოლური და დიასტოლური წნევა.
# პროგრამამ უნდა განსაზღვროს წნევის კატეგორია:

# თუ სისტოლური > 140 ან დიასტოლური > 90 → "მაღალი წნევა"

# თუ სისტოლური < 90 ან დიასტოლური < 60 → "დაბალი წნევა"

# სხვა შემთხვევაში → "წნევა ნორმალურია"
# მომხმარებელი შეიყვანს წნევის მონაცემებს
systolic = int(input("შეიყვანეთ სისტოლური წნევა: "))
diastolic = int(input("შეიყვანეთ დიასტოლური წნევა: "))

# შეფასება
if systolic > 140 or diastolic > 90:
    print("მაღალი წნევა")
elif systolic < 90 or diastolic < 60:
    print("დაბალი წნევა")
else:
    print("წნევა ნორმალურია")

#  წონის შეფასება ასაკის მიხედვით (უბრალოდ და ლოგიკურად)
#  აღწერა:
# მომხმარებელი შეიყვანს საკუთარ ასაკს და წონას.
# პროგრამამ უნდა შეაფასოს წონა ასაკის მიხედვით მარტივი ლოგიკით:

# თუ ასაკი < 10:

# წონა < 20 → "წონა დაბალია"

# წონა 20-40 → "წონა ნორმალურია"

# წონა > 40 → "წონა მაღალია"

# თუ ასაკი 10-17:

# წონა < 40 → "წონა დაბალია"

# წონა 40-65 → "წონა ნორმალურია"

# წონა > 65 → "წონა მაღალია"

# თუ ასაკი 18 ან მეტი:

# წონა < 50 → "წონა დაბალია"

# წონა 50-90 → "წონა ნორმალურია"

# წონა > 90 → "წონა მაღალია"

# მომხმარებელი შეიყვანს ასაკს და წონას
age = int(input("შეიყვანეთ თქვენი ასაკი: "))
weight = int(input("შეიყვანეთ თქვენი წონა (კგ): "))

# შეფასება ასაკისა და წონის მიხედვით
if age < 10:
    if weight < 20:
        print("წონა დაბალია")
    elif 20 <= weight <= 40:
        print("წონა ნორმალურია")
    else:
        print("წონა მაღალია")
elif 10 <= age <= 17:
    if weight < 40:
        print("წონა დაბალია")
    elif 40 <= weight <= 65:
        print("წონა ნორმალურია")
    else:
        print("წონა მაღალია")
else:  # ასაკი 18 ან მეტი
    if weight < 50:
        print("წონა დაბალია")
    elif 50 <= weight <= 90:
        print("წონა ნორმალურია")
    else:
        print("წონა მაღალია")


#  ასაკის შეჯერება დღის მონაკვეთთან
# შეიყვანოს მომხმარებელმა ასაკი და საათი (0-დან 23-მდე). პროგრამამ განსაზღვროს:

# თუ ასაკი < 18 და დრო ≥ 22 → "დროა დაძინების"

# თუ ასაკი ≥ 60 და დრო ≥ 21 → "დასვენება რეკომენდებულია"

# სხვა შემთხვევაში → "შეგიძლიათ გააგრძელოთ აქტივობა"

# მომხმარებელი შეიყვანს ასაკს და საათს
age = int(input("შეიყვანეთ თქვენი ასაკი: "))
hour = int(input("შეიყვანეთ მიმდინარე საათი (0-დან 23-მდე): "))

# შეფასება ასაკისა და დროის მიხედვით
if age < 18 and hour >= 22:
    print(" დროა დაძინების!")
elif age >= 60 and hour >= 21:
    print(" დასვენება რეკომენდებულია!")
else:
    print(" შეგიძლიათ გააგრძელოთ აქტივობა")


# ფიზიკური აქტივობის რეკომენდაცია
# მომხმარებელი შეიყვანს თავის ასაკს და გულის ცემას.
# პროგრამამ უნდა დაარიგოს ასეთი რჩევები:

# ასაკი < 30 და გულისცემა < 140 → "შეგიძლიათ მეტი ივარჯიშოთ"

# ასაკი ≥ 30 და გულისცემა > 170 → "დასვენება გჭირდებათ"

# სხვა შემთხვევაში → "აქტივობის დონე ნორმალურია"


# მომხმარებელი შეიყვანს ასაკს და გულისცემას
age = int(input("შეიყვანეთ თქვენი ასაკი: "))
heart_rate = int(input("შეიყვანეთ გულისცემა (BPM): "))

# რეკომენდაცია აქტივობის შესახებ
if age < 30 and heart_rate < 140:
    print(" შეგიძლიათ მეტი ივარჯიშოთ")
elif age >= 30 and heart_rate > 170:
    print(" დასვენება გჭირდებათ")
else:
    print(" აქტივობის დონე ნორმალურია")


#  ასაკი და კვება
# მომხმარებელი შეიყვანს ასაკს. პროგრამა გასცემს კვების რეკომენდაციას:

# 0–12 → "ბევრი ვიტამინიანი საკვები"

# 13–25 → "ენერგიის მომცემი საკვები"

# 26–59 → "ბალანსირებული რაციონი"

# 60+ → "დაბალკალორიული და მსუბუქი საკვები"

# მომხმარებელი შეჰყავს ასაკს
age = int(input("შეიყვანეთ თქვენი ასაკი: "))

# რეკომენდაციის განსაზღვრა
if 0 <= age <= 12:
    print(" ბევრი ვიტამინიანი საკვები")
elif 13 <= age <= 25:
    print(" ენერგიის მომცემი საკვები")
elif 26 <= age <= 59:
    print(" ბალანსირებული რაციონი")
elif age >= 60:
    print(" დაბალკალორიული და მსუბუქი საკვები")
else:
    print(" არასწორი ასაკი")



